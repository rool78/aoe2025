class Solution1 {

    private var dialPos = 50
    private val rightDirection = 'R'

    operator fun invoke(input: String): Int  {
        var password = 0

        val list = input.split("\n")

        list.forEach {
            val direction = it.toCharArray()[0]
            val distance = it.substring(1).toInt()
            if (direction == rightDirection) {
                dialPos += distance
            } else {
                dialPos -= distance
            }
            if (dialPos % 100 == 0) password++
        }

        return password
    }

}


