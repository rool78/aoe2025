fun solve(input: String) : Long {
    var sol = 0L
    val data = input.split("\n\n").let {
        val ranges = it[0]
        val values = it[1]
        ranges to values
    }
    val ranges = data.first.split('\n').map {
        val s = it.split('-')
        Solution2.Range(s[0].toLong(), s[1].toLong())
    }

    data.second.split('\n').forEach { id ->
        val match = ranges.any { r ->
            val idn = id.toLong()
            idn >= r.start && idn <= r.end
        }
        if (match) sol++
    }

    return sol
}